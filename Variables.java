// Lecture 3 - Variables in java
public class Variables {
    public static void main(String[] args) {
        //  A variable is a name given to a memory location in a program. It is used to store data that can be changed during the execution of the program. Variables are used to hold values that can be referenced and manipulated in a program. In Java, variables are created when a value is assigned to them using the assignment operator `=`. 
        
        // Example 

        int a = 10;        // int -> datatype, a -> variable, 10 -> value/data 
        int b = 5;         // b -> variable, 5 -> value/data

        System.out.println(a);
        System.out.println(b);

        String name = "Piyush bhatt";
        System.out.println(name);
    }
}
